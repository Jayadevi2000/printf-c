#include "libft.h"

void	ft_striteri(char *s, void (*f)(unsigned int, char*))
{
	char	*c;
	size_t	i;

	i = 0;
	while (s[i] != '\0')
	{
		c = &s[i];
		(*f)(i, c);
		i++;
	}
}
