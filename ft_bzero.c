#include "libft.h"

void	ft_bzero(void *s, size_t n)
{
	char			*cadena;
	size_t			i;

	cadena = (char *) s;
	i = 0;
	while (i < n)
	{
		cadena[i] = '\0';
		i++;
	}	
}
